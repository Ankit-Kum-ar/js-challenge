let num1 = 55;
let num2 = 50;

// Using += and -= operators...
num1 += num2;
console.log("Addition is",num1);

num1 -= num2;
console.log("Subtraction is",num1);

// Using *= and /= operators...
num1 *= num2;
console.log("Multiplication is",num1);

num1 /= num2;
console.log("Division is",num1);

// Using %= operator...
num1 %= num2;
console.log("Remainder is",num1);

// Output:
// Addition is 105
// Subtraction is 55
// Multiplication is 2750
// Division is 55
// Remainder is 5

// Note: The value of num1 is updated after each operation.