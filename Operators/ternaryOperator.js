let num1 = 42;
let num2 = 50;

// Using ? : operator...
let result = num1 > num2 ? "Greater" : "Smaller";
console.log(result);

// Output: Smaller
// Note: The value of result is based on the condition num1 > num2. If the condition is true, the value is "Greater", otherwise it is "Smaller". In this case, the condition is false, so the output is "Smaller".